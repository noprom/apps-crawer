from scrapy import signals
import json
import codecs

class JsonWithEncodingHiApkPipeline(object):

    def __init__(self):
        self.file = codecs.open('hiapk.json', 'w', encoding='utf-8')

    def process_item(self, item, spider):
        line = json.dumps(dict(item)) + "\n"
        self.file.write(line.decode('unicode_escape'))
        return item

